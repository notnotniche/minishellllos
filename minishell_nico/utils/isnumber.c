#include "../minishell.h"

int	is_alphanumerical(char c)
{
	if ((c >= 7 && c <= 13) || c == '\0' || c == 32)
		return (0);
	return (1);
}

int is_quote(char c)
{
	if ((c == '\'') || (c == '\"'))
		return (1);
	return (0);
}