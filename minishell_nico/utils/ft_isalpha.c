#include "../minishell.h"

int	ft_isalpha(char c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

int ft_isalnum(char c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c  <= 122) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}

int ft_isitenv(char c)
{
	if ((c == '_' || c == '?' || c == '\'' || c == '\"'))
		return (1);
	return (0);
}